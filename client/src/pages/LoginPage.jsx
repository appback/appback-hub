import React, { useState, useEffect } from 'react'
import { useNavigate, useSearchParams } from 'react-router-dom'
import { publicApi, setAuth, getUser } from '../api'
import { useToast } from '../components/Toast'

const GITHUB_AUTH_URL = '/api/v1/auth/github?redirect=https://appback.app/auth/callback'
const GOOGLE_AUTH_URL = '/api/v1/auth/google?redirect=https://appback.app/auth/callback'

export default function LoginPage() {
  const navigate = useNavigate()
  const toast = useToast()
  const [searchParams] = useSearchParams()
  const [mode, setMode] = useState('login')
  const [email, setEmail] = useState('')
  const [password, setPassword] = useState('')
  const [displayName, setDisplayName] = useState('')
  const [loading, setLoading] = useState(false)

  // If already logged in, redirect
  useEffect(() => {
    if (getUser()) navigate('/', { replace: true })
  }, [])

  // Handle token from OAuth callback redirect
  useEffect(() => {
    const token = searchParams.get('token')
    if (token) {
      // Fetch user info with the token
      localStorage.setItem('hub_token', token)
      import('../api').then(({ authApi }) => {
        authApi.get('/auth/me')
          .then(res => {
            setAuth(token, res.data.user)
            toast.success('Logged in!')
            navigate('/', { replace: true })
          })
          .catch(() => {
            localStorage.removeItem('hub_token')
            toast.error('Failed to verify token')
          })
      })
    }
  }, [searchParams])

  async function handleSubmit(e) {
    e.preventDefault()
    setLoading(true)

    try {
      const url = mode === 'login' ? '/auth/login' : '/auth/register'
      const body = mode === 'login'
        ? { email, password }
        : { email, password, display_name: displayName || undefined }

      const res = await publicApi.post(url, body)
      const { user, token } = res.data

      setAuth(token, user)
      toast.success(mode === 'login' ? 'Logged in!' : 'Account created!')
      navigate(user.role === 'admin' ? '/admin' : '/', { replace: true })
    } catch (err) {
      toast.error(err.response?.data?.message || 'Authentication failed')
    } finally {
      setLoading(false)
    }
  }

  return (
    <div className="container animate-fade-in">
      <div className="page-header" style={{ textAlign: 'center' }}>
        <h1>{mode === 'login' ? 'Welcome Back' : 'Create Account'}</h1>
        <p className="page-subtitle">Sign in to Appback Hub</p>
      </div>

      <div style={{ maxWidth: '400px', margin: '0 auto' }}>
        <a href={GOOGLE_AUTH_URL} className="btn btn-lg" style={{ width: '100%', marginBottom: '12px', background: '#fff', color: '#444', border: '1px solid #ddd' }}>
          <svg viewBox="0 0 24 24" width="20" height="20" style={{ marginRight: '8px', verticalAlign: 'middle' }}>
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 0 1-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z"/>
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
          </svg>
          Continue with Google
        </a>

        <a href={GITHUB_AUTH_URL} className="btn btn-github btn-lg" style={{ width: '100%', marginBottom: '24px' }}>
          <svg viewBox="0 0 16 16" width="20" height="20" style={{ marginRight: '8px', verticalAlign: 'middle' }}>
            <path fill="currentColor" d="M8 0C3.58 0 0 3.58 0 8c0 3.54 2.29 6.53 5.47 7.59.4.07.55-.17.55-.38 0-.19-.01-.82-.01-1.49-2.01.37-2.53-.49-2.69-.94-.09-.23-.48-.94-.82-1.13-.28-.15-.68-.52-.01-.53.63-.01 1.08.58 1.23.82.72 1.21 1.87.87 2.33.66.07-.52.28-.87.51-1.07-1.78-.2-3.64-.89-3.64-3.95 0-.87.31-1.59.82-2.15-.08-.2-.36-1.02.08-2.12 0 0 .67-.21 2.2.82.64-.18 1.32-.27 2-.27.68 0 1.36.09 2 .27 1.53-1.04 2.2-.82 2.2-.82.44 1.1.16 1.92.08 2.12.51.56.82 1.27.82 2.15 0 3.07-1.87 3.75-3.65 3.95.29.25.54.73.54 1.48 0 1.07-.01 1.93-.01 2.2 0 .21.15.46.55.38A8.013 8.013 0 0016 8c0-4.42-3.58-8-8-8z"/>
          </svg>
          Continue with GitHub
        </a>

        <div className="divider"><span>or</span></div>

        <form onSubmit={handleSubmit}>
          {mode === 'register' && (
            <div className="form-group">
              <label className="form-label">Display Name</label>
              <input className="form-input" type="text" value={displayName}
                onChange={e => setDisplayName(e.target.value)} placeholder="Your nickname" />
            </div>
          )}

          <div className="form-group">
            <label className="form-label">Email</label>
            <input className="form-input" type="email" value={email}
              onChange={e => setEmail(e.target.value)} required placeholder="email@example.com" />
          </div>

          <div className="form-group">
            <label className="form-label">Password</label>
            <input className="form-input" type="password" value={password}
              onChange={e => setPassword(e.target.value)} required minLength={6} placeholder="Min 6 characters" />
          </div>

          <button className="btn btn-primary btn-lg" style={{ width: '100%' }} disabled={loading} type="submit">
            {loading ? 'Please wait...' : mode === 'login' ? 'Login' : 'Register'}
          </button>
        </form>

        <div className="text-center mt-md">
          <button className="btn btn-ghost btn-sm"
            onClick={() => setMode(mode === 'login' ? 'register' : 'login')}>
            {mode === 'login' ? 'Need an account? Register' : 'Have an account? Login'}
          </button>
        </div>
      </div>
    </div>
  )
}
